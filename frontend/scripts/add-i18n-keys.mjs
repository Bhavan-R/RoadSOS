// Patch each bundle.
let updated = 0;
for (const code of Object.keys(T)) {
  const file = path.join(I18N_DIR, `${code}.json`);
  const data = JSON.parse(fs.readFileSync(file, 'utf8'));
  let changed = false;
  for (const key of NEW_KEYS) {
    if (!(key in data)) {
      data[key] = T[code][key];
      changed = true;
    }
  }
  if (changed) {
    fs.writeFileSync(file, JSON.stringify(data, null, 2) + '\n', 'utf8');
    updated += 1;
  }
}
